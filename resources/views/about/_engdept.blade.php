{{-- SHORT HISTORY --}}
<section style="padding:72px 0;background:var(--off-white)">
  <div class="container reveal">
    <h2 class="sec-heading"><span class="underline">&#128221; Sejarah Singkat</span></h2>
    <div style="max-width:720px;margin:0 auto">
      <p style="font-size:.88rem;color:var(--gray);line-height:1.8;margin-bottom:32px;text-align:center">
        The history of the English Department at Politeknik Negeri Bandung (POLBAN) began with an initiative from a group of English lecturers under the General Courses Unit (UP-MKU). Inspired to establish a Diploma III (D-3) English Study Program, they worked through a series of planning, proposal development, and institutional approval processes before the department was officially established.
      </p>
      <div class="timeline">
        <div class="timeline-item">
          <div class="timeline-dot"></div>
          <div class="timeline-year">1997</div>
          <div class="timeline-text">The D-1 <em>English for Secretary</em> Program was established under the leadership of Dra. Ratna Padmi Trihartanti. The program operated for one year before being discontinued due to regulations that did not permit Politeknik ITB to offer D-1 programs.</div>
        </div>
        <div class="timeline-item">
          <div class="timeline-dot"></div>
          <div class="timeline-year">2004</div>
          <div class="timeline-text">The Head of UP-MKU issued an assignment letter appointing four lecturers to form a team responsible for preparing the proposal for the establishment of the D-3 English Study Program.</div>
        </div>
        <div class="timeline-item">
          <div class="timeline-dot"></div>
          <div class="timeline-year">2006</div>
          <div class="timeline-text">The proposal was approved by the Directorate General of Higher Education through Decree No. 2681/D/T/2006, granting operational authorization for the D-3 English Study Program. POLBAN officially opened its first admission with 50 applicants in its inaugural intake.</div>
        </div>
        <div class="timeline-item">
          <div class="timeline-dot"></div>
          <div class="timeline-year">2008</div>
          <div class="timeline-text">The English Department was officially established through a decree issued by the Director of Politeknik Negeri Bandung, creating opportunities for the development of new English-related academic programs.</div>
        </div>
        <div class="timeline-item">
          <div class="timeline-dot"></div>
          <div class="timeline-year">2012</div>
          <div class="timeline-text">The D-3 English Study Program received an <strong>"A"</strong> accreditation from BAN-PT, marking a significant achievement in the department's history.</div>
        </div>
        <div class="timeline-item">
          <div class="timeline-dot"></div>
          <div class="timeline-year">2022</div>
          <div class="timeline-text">The D-3 English Study Program earned a <strong>"Very Good" (Baik Sekali)</strong> accreditation from BAN-PT, reflecting its continued commitment to excellence in vocational education.</div>
        </div>
      </div>
    </div>
  </div>
</section>

